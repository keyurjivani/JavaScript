const ui = (Products) =>{
    document.getElementById('Product').innerHTML = ""
    Products.map((ele,i)=>{
        let img = document.createElement('img');
        img.src = ele.img
        img.setAttribute("class","h-full w-full object-cover object-center group-hover:opacity-75")

        let div_img = document.createElement('div');
        div_img.append(img)
        div_img.setAttribute("class","h-3/5 aspect-h-1 aspect-w-1 w-full overflow-hidden rounded-lg bg-gray-200 xl:aspect-h-8 xl:aspect-w-7")

        let name = document.createElement('h3');
        name.innerHTML = ele.name
        name.setAttribute("class","mt-4 text-sm text-white")

        let price = document.createElement('p');
        price.innerHTML = ele.price
        price.setAttribute("class","t-1 text-lg font-medium text-white")

        let icon = document.createElement('i');
        icon.setAttribute("class","mt-1 text-lg font-medium text-white")
        icon.innerHTML = `<i class="fa-solid fa-star"><i class="fa-solid fa-star"><i class="fa-solid fa-star"><i class="fa-solid fa-star"><i class="fa-solid fa-star"><i class="fa-solid fa-star">`

        let br = document.createElement('br');
        br.innerHTML = "<br>";

        let button = document.createElement('button');
        button.innerHTML = "Add to Cart"
        button.setAttribute("class","text-black bg-yellow-500 font-medium tracking-widest px-6 rounded-lg")
        button.addEventListener("click", () =>PushProduct(ele))

        let a = document.createElement('a');
        a.append(div_img,name,price,icon,br,button)
        a.setAttribute("class","group")



        document.getElementById('Product').append(a)
    })
}
ui(Products)